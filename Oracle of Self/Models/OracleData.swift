//
//  OracleData.swift
//  Oracle of Self
//
//  Static content for the Oracle of Self quiz: 10 question slots x 100
//  variants, plus 11 score buckets x 100 outcome variants each.
//
//  Each slot probes a different decision-making angle (hidden willingness,
//  positive visualization, bodily response, regret vs. fear, etc.).
//  `generateQuestions()` picks one variant per slot so a session always
//  asks all ten angles in order but never feels like a fixed script.
//
//  Score range: 0–10 trues. `outcome(for:)` indexes into `outcomeVariants`
//  by score and returns one of 100 phrasing variants at random so the same
//  score can deliver thousands of distinct readings.
//

import Foundation

// MARK: - Question

/// A single Oracle prompt. The user answers true or false.
struct OracleQuestion: Identifiable {
    let id = UUID()
    let text: String
}

// MARK: - Session State

/// Drives `OracleView`'s switch between prompt, question, and result screens.
enum OracleState {
    /// Pre-quiz landing screen. User types the question they want answered.
    case prompt
    /// Mid-quiz; associated value is the index of the active question (0-based).
    case asking(currentQuestion: Int)
    /// Post-quiz; associated value is the final true count (0–10).
    case result(score: Int)
}

